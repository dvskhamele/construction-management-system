// Define the type for our translations
type TranslationKeys = keyof typeof translations.en;
type Locale = keyof typeof translations;

// Get the translation for a specific key and locale
export const t = (key: TranslationKeys, locale: Locale | string = 'en'): string => {
  const lang = translations[locale as Locale] || translations['en'];
  return lang[key as TranslationKeys] || key;
};

// Get all available locales
export const getAvailableLocales = (): string[] => {
  return Object.keys(translations);
};